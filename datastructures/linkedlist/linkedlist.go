package linkedlist

// LList represents a linked list
type LList struct {
	Head Node
	Tail Node
}

// DLList represents a doubly linked list
type DLList struct {
	Head Node
	Tail Node
}

// CLList represents a circular linked list
type CLList struct {
	Head Node
	Tail Node
}

// CDLList represents a circular doubly linked list
type CDLList struct {
	Head Node
	Tail Node
}
